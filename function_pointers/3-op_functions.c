#include "3-calc.h"

/**
 * op_add - returns the sum of a and b
 * @a: num1
 * @b: num2
 *
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of a and b
 * @a: num1
 * @b: num2 to substract
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of a and b
 * @a: num1
 * @b: num2
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the div of a by b
 * @a: num1 to divide
 * @b: num2
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the modulo of a by b
 * @a: num1
 * @b: num2
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}

