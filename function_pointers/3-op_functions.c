#include "3-calc.h"

/**
 * op_add - returns the sum of a and b
 * @a: num1
 * @b: num2
 *
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of a and b
 * @a: num1
 * @b: num2 to substract
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}


